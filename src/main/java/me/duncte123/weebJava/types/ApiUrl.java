package me.duncte123.weebJava.types;

public enum ApiUrl {
    PRODUCTION("https://api.weeb.sh"),
    STAGING("https://staging.weeb.sh");

    private final String url;
    ApiUrl(String url) {
        this.url = url;
    }

    public String getUrl() {
        return url;
    }

    @Override
    public String toString() {
        return url;
    }
}
